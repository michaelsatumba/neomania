import Head from 'next/head';
import { useState } from 'react';

export default function Home() {
	const [exercise, setExercise] = useState('Exercise');
	const [input, setInput] = useState('');
	const [arrayExercises, setArrayExercises] = useState([
		'Stretch',
		'Strength',
		'Power Clean',
		'Bodybuild',
		'Calisthenics',
		'Walk',
		'Jog',
		'Sprint',
		'Rest',
		'Foam Roll',
		'Walking with Backpack',
		'Swim',
		'Jump Rope',
		'Yoga',
		'Bike',
		'Basketball',
		'Rollerskate',
		'Hike',
		'Bat',
		'Dance',
	]);

	const generate = () => {
		const randomNumber = Math.floor(Math.random() * arrayExercises.length);
		const randomExercise = arrayExercises[randomNumber];
		setExercise(randomExercise);
	};

	const handleSubmit = (e) => {
		e.preventDefault();
		setArrayExercises([...arrayExercises, input]);
		setInput('');
	};

	const deleteExercise = (id) => {
		// alert(index);
		const arr = arrayExercises.filter(
			(exercise, exerciseIndex) => exerciseIndex != id
		); // deletes the index
		setArrayExercises(arr);
	};

	// const deleteItem = (index) => {
	// 	const arr = items.filter((item, itemIndex) => itemIndex != index); // deletes the index
	// 	setItems(arr);
	// };

	return (
		<div>
			<Head>
				<title>Neomania</title>
				<meta name="description" content="Generated by create next app" />
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<div className="flex flex-col items-center justify-center h-screen bg-gradient-to-r from-indigo-500 to-blue-500">
				<div>
					<p>Exercises:</p>
					{arrayExercises.map((n, id) => (
						<p onClick={() => deleteExercise(id)} key={id}>
							{n}
						</p>
					))}
				</div>
				<form className="flex space-x-2" onSubmit={handleSubmit}>
					<input
						type="text"
						className="bg-gray-500 rounded-full my-2 px-4 py-2"
						value={input}
						placeholder={`Add Exercise`}
						onChange={(e) => setInput(e.target.value)}
					/>
				</form>
				<h1 className="mb-4">Do this today:</h1>
				<p
					// className="animate-spin"
					// className="animate-ping"
					// className="animate-pulse"
					className="animate-bounce"
				>
					{exercise}
				</p>
				<button
					onClick={generate}
					className="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded-full mt-4"
				>
					Click Me
				</button>
			</div>
		</div>
	);
}
